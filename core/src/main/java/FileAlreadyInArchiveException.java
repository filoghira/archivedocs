public class FileAlreadyInArchiveException extends Exception {
    public FileAlreadyInArchiveException() { super(); }
    public FileAlreadyInArchiveException(String message) { super(message); }
    public FileAlreadyInArchiveException(String message, Throwable cause) { super(message, cause); }
    public FileAlreadyInArchiveException(Throwable cause) { super(cause); }
}
